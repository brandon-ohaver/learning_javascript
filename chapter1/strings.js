// strings represent text and are created by enclosing their contents in qutoes
`Life is a highway`
"Time to dig deap"
'Get out of here'

// in order to use special characters in a string like a quote or a new line, use the escaping character technique (\)
"This is my dad\'s favorite painting"
"Time for a new story\nThe story begins anew"

// The following shows how to close out an escaping character so that it can be visible in the string
"A newline character is written like \"\\n\"." // prints: A newline character is written like "\n."

// strings can be concatenated or "glued together" by adding them
"con" + "cat" + "e" + "nate" // this will produce "concatenate"

// using backtick-quotes or template literals allows the embedding of values
`half of 100 is ${100 / 2}`


